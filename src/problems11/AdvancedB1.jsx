import { DigitPicker } from '../components/DigitPicker'

const CARDS9 = [1, 2, 3, 4, 5, 6, 7, 8, 9].map((v, i) => ({ id: `c${i}`, value: v }))

export default function AdvancedB1() {
  return (
    <div className="problem">
      <h2>応用問題B-1　カードで3けたの整数を作る</h2>
      <div className="statement">
        <ol className="question-list">
          <li>{'{1，2，3，4，5，6，7，8，9}'}の9枚のカードがあります。このうちの3枚をならべて3けたの整数を作ります。整数は何通りできますか。</li>
          <li>{'{1，2，…，15}'}の15枚のカードがあります。このうちの2枚または3枚をならべて3けたの整数を作ります。整数は何通りできますか。たとえば，「2」「15」と「2」「1」「5」はどちらも同じ「215」を表します。</li>
        </ol>
      </div>

      <h3>(1) 9枚から3枚選ぶ</h3>
      <DigitPicker cards={CARDS9} pickCount={3} />

      <div className="explain">
        <h3>解説</h3>
        <p>(1) 9×8×7＝<b>504(通り)</b></p>
        <p>
          (2) 3枚をならべて3けたの数を作る…(1)と同じなので504通り。2枚をならべて3けたの数を作るとき，(1)と重複しないように注意する必要があります。
        </p>
        <p>10を使う…重複なし。10□ → 9通り，□10 → 9通り。</p>
        <p>11を使う…重複なし。11□ → 9通り，□11 → 8通り（11□と□11で111が重複します）。</p>
        <p>12を使う…重複あり。12□ → □は1，2のみ可となり，2通り。□12 → □は2のみ可となり，1通り（112は11を使うときと重複します）。13，14，15を使うときもこれと同様です。</p>
        <p>以上より，504＋9×3＋8＋(2＋1)×4＝<b>551(通り)</b></p>
      </div>
    </div>
  )
}
